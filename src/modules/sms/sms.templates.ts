/**
 * SMS message templates, ported verbatim (Nepali) from the v1 webapp.
 *
 * Placeholders use `{token}` syntax and are filled by `renderTemplate`.
 * Supported tokens: {name}, {patientId}.
 */

export type SmsTemplateKey =
  | "patientRegistration"
  | "pregnancyRegistration"
  | "imnciRegistration";

export type SmsTemplateVars = Record<
  string,
  string | number | null | undefined
>;

/**
 * Instant, event-triggered welcome messages. Note: the OPD patient-registration
 * and IMNCI-registration copy are identical in v1 — kept as separate keys so
 * either can diverge later without touching call sites.
 */
